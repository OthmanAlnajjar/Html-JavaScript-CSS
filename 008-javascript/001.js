// Sonderaktionen durch Escapen
/* Escapen wird auch zur Darstellung von Yeichen benutzt, die nicht auf der Tastatur eingegeben werden können
und im Dokument nicht direkt sichtbar sind, sonder nur durch ihre Wirkung in Erscheinung treten.
Dazu zählen \n für einen Zeilenumbruch und \t für einen Tabulatorsprung. */
