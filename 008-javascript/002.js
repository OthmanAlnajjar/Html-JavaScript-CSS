// Variablen in JavaScript
// im Prinzip Buchtaben, kein Punkt, kein Bindestrich
// Unterstich _ ist erlaubt und zwar Überall egal am Angang/Ende.
// CaseSensitive: CamelArt ist andere Variable als Camelart.
// Variablen werden mit let definiert z.b:

let inhalt;
inhalt = "Guten Morgen";
console.log(inhalt);

let inhalt2 = "Nur Hallo";
console.log(inhalt2);

// Eine variable kann immer überschieben werden, d.h. es kann ein neuer Inhalt zugewiesen
// nur eine Zuweisung deswegen ohne let (wird nicht neu definiert)
inhalt= "neuer inhalt";
console.log(inhalt);
